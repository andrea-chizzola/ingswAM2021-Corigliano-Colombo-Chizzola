package it.polimi.ingsw.Model.Resources;

/**
 * This class identifies the colors of the resources
 * It is an enumeration
 */
public enum ResourceColor {
    /**
     * This color represents the resource shield
     */
    BLUE,

    /**
     * This color represents the resource stone
     */
    GRAY,

    /**
     * This color represents the resource coin
     */
    YELLOW,

    /**
     * This color represents the resource servant
     */
    PURPLE,

    /**
     * This color represents faith points
     */
    RED,

    /**
     * This color represents an empty resource
     */
    WHITE
}