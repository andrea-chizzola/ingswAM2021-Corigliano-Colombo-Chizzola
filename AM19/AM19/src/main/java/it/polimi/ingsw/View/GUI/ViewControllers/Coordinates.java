package it.polimi.ingsw.View.GUI.ViewControllers;

public class Coordinates {

    private double x;
    private double y;

    public Coordinates(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX(){
        return x;
    }

    public double getY() {
        return y;
    }
}
